let currentLang = localStorage.getItem("lang") || "en";

function t(path) {
  return path.split(".").reduce((obj, key) => obj?.[key], translations[currentLang]) || path;
}

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("lang", lang);
  updateAllTranslations();
  updateLanguageButtons();
}

function toggleLanguage() {
  setLanguage(currentLang === "en" ? "es" : "en");
}

function updateLanguageButtons() {
  document.querySelectorAll("[data-lang-toggle]").forEach(btn => {
    const label = btn.querySelector("[data-lang-label]");
    if (label) {
      label.textContent = currentLang === "en" ? "ES" : "EN";
    }
  });
}

function updateAllTranslations() {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-html]").forEach(el => {
    const key = el.dataset.i18nHtml;
    if (key === "home.exploreOurSabor") {
      el.innerHTML = t("home.exploreOurSabor") + ' <em class="text-yellow-400">' + t("home.sabor") + "</em>";
    }
  });
}
